package level3.model;

public class Calculator {

    private float a;
    private float b;

    public float add(float a, float b){
        float result = a + b;
        return result;
    }

    public float substract(float a, float b){
        float result = a - b;
        return result;
    }

    public float mul(float a, float b){
        float result = a * b;
        return result;
    }

    public float div(float a, float b){
        float result = a / b;
        return result;
    }




}
